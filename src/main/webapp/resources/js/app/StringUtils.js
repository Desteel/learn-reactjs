String.prototype.format = String.prototype.f = function () {
    var args = arguments;
    return this.replace(/\{\{|\}\}|\{(\d+)\}/g, function (m, n) {
        if (m == "{{") { return "{"; }
        if (m == "}}") { return "}"; }
        return args[n];
    });
};

String.prototype.equalsIgnoreCase = String.prototype.eqIgnoreCase = function(str){
   return str.toLowerCase() === this.toLowerCase();
};